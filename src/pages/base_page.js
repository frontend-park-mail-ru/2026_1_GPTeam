/**
 * Абстрактный базовый класс для всех страниц приложения.
 * Обеспечивает единый интерфейс жизненного цикла (render/destroy) 
 * и автоматическое управление вложенными компонентами.
 * * @class BasePage
 */
export class BasePage {
    /**
     * Конструктор BasePage.
     * Инициализирует внутреннее хранилище для отслеживания дочерних компонентов.
     */
    constructor() {
        /**
         * Список активных дочерних компонентов на текущей странице.
         * Используется для рекурсивной очистки ресурсов при смене маршрута.
         * * @type {Array<BaseComponent>}
         * @protected
         */
        this._components = [];
    }

    /**
     * Асинхронный метод для отрисовки страницы в корневой элемент.
     * Предназначен для обязательного переопределения в дочерних классах (LoginPage, BudgetPage и т.д.).
     * * @async
     * @param {HTMLElement} _root - Корневой DOM-элемент, в который будет помещено содержимое страницы.
     * @returns {Promise<void>}
     */
    async render(_root) {
        // Метод переопределяется в наследниках для реализации конкретной логики страницы
    }

    /**
     * Производит полную очистку страницы перед её уничтожением.
     * Вызывает метод destroy() у каждого зарегистрированного дочернего компонента 
     * и обнуляет массив компонентов.
     * * @returns {void}
     */
    destroy() {
        this._components.forEach((c) => {
            if (c && typeof c.destroy === 'function') {
                c.destroy();
            }
        });
        this._components = [];
    }
}